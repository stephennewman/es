// Helper function to get random principles
export function getRandomPrinciples(count: number = 3): Principle[] {
  const shuffled = [...principles].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

// Helper function to get principles by category
export function getPrinciplesByCategory(category: string): Principle[] {
  return principles.filter((p) => p.category === category);
}

